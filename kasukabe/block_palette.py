def get_color(block_id: str) -> tuple[int, int, int]:
    """Get RGB color for a block ID.

    Strips blockstate properties (e.g. ``oak_stairs[facing=north]`` -> ``minecraft:oak_stairs``).
    Returns FALLBACK_COLOR (magenta) for unknown blocks.
    """
    clean = block_id.split("[")[0]
    return BLOCK_COLORS.get(clean, FALLBACK_COLOR)
